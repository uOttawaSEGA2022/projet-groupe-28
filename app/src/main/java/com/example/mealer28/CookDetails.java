package com.example.mealer28;

public class CookDetails {

    public String fullname, email, password, address, voidcheck, description;


    public CookDetails(){}

    public CookDetails(String fullname, String email, String password, String address, String voidcheck, String description){
        this.fullname = fullname;
        this.email = email;
        this.password = password;
        this.address = address;
        this.voidcheck = voidcheck;
        this.description = description;
    }


}
